    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // BOTON GUARDAR
        Date Fecha_original = txtfecha.getDate();
        SimpleDateFormat formatoFecha_envio_de = new SimpleDateFormat("dd-MM-yyyy");
        String fecha_format = formatoFecha_envio_de.format(Fecha_original);
        //System.out.println(fecha_format);

        Registros r = new Registros();
        r.setId(txtid.getText());
        r.setNombre(txtnombre.getText());
        r.setApellido_paterno(txtap.getText());
        r.setApellido_materno(txtam.getText());
        r.setFecha_nacimiento(fecha_format);
        r.setEmail(txtcorreo.getText());

        int opcion = JOptionPane.showConfirmDialog(null, "Estas seguro de que quieres guardar el registro", "Confirmar", JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_NO_OPTION) {
            DaoRegistros dar = new DaoRegistros();
            int res = dar.insertar(r);
            if (!dar.isIsError() && res != 0) {

                JOptionPane.showMessageDialog(null, "SE AGREGO CORRECTAMENTE. ");
            } else {

                JOptionPane.showMessageDialog(null, "NO SE PUDO AGREGAR, error: " + dar.getError());
            }

        } else {
            JOptionPane.showMessageDialog(null, "No se realizo ninguna modificación");
        }

        modeloRegistros.getDataVector().removeAllElements();
        TablaRegistros.updateUI();

        DaoRegistros da = new DaoRegistros();
        da.clean();
        da.buscartabla(TablaRegistros);


    }//GEN-LAST:event_btnGuardarActionPerformed

