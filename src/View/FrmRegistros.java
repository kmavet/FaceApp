    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // BOTON PARA ELIMINAR UN REGISTRO

        int opcion = JOptionPane.showConfirmDialog(null, "Estas seguro que quieres eliminar el registro", "confirmar", JOptionPane.YES_NO_OPTION);

        if (opcion == JOptionPane.YES_NO_OPTION) {
            if (txtid.getText().toString().equals("") || txtid.getText() == null) {
                JOptionPane.showMessageDialog(null, "NO SE PUEDE ELIMINAR SI NO HAY ID");;
            } else {
                DaoRegistros dar = new DaoRegistros();
                int resultado = dar.eliminar(txtid.getText());
                if (!dar.isIsError() || resultado != 0) {
                    JOptionPane.showMessageDialog(null, "SE ELIMINO CORRECTAMENTE");
                } else {
                    JOptionPane.showMessageDialog(null, "NO SE PUDO ELIMAR EL REGISTRO, error: " + dar.getError());
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "NO SE REALIZO NINGUNA MODIFICACIÓN");
        }

        modeloRegistros.getDataVector().removeAllElements();
        TablaRegistros.updateUI();
        DaoRegistros da = new DaoRegistros();
        da.clean();
        da.buscartabla(TablaRegistros);
    }//GEN-LAST:event_btnEliminarActionPerformed

