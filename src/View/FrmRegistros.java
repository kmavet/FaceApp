    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // BOTON BUSCAR

        DaoRegistros e = new DaoRegistros();
        Registros em = e.buscar(txtid.getText());
        
        String fecha = em.getFecha_nacimiento();
        DateFormat formatoFecha = new SimpleDateFormat("dd-MM-yyyy");
        Date fechaNacimientoDate = null;
        try {
            fechaNacimientoDate = formatoFecha.parse(fecha);
        } catch (ParseException ex) {
            Logger.getLogger(FrmRegistros.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Calendar fechaNacimientoCal = Calendar.getInstance();
        fechaNacimientoCal.setTime(fechaNacimientoDate);

        if (txtid.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo vacio");
        } else {
            if (!e.isIsError() && em != null) {
                if (!em.getId().equals("")) {
                    txtid.setText(em.getId());
                    txtnombre.setText(em.getNombre());
                    txtap.setText(em.getApellido_paterno());
                    txtam.setText(em.getApellido_materno());
                    txtfecha.setCalendar(fechaNacimientoCal);
                    txtcorreo.setText(em.getEmail());

                }
            } else {
                JOptionPane.showMessageDialog(null, "NO SE PUEDE ENCONTRAR, ERROR: " + e.getError());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

